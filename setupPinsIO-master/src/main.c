// Proposito: Piscar LED para teste da ESP32
//
//   Entrada: void *arg
//     Saída: NULL
/////////////////////////////////////////////////////////////////////////////
void xLED(void *arg) {

    uint8_t led = DESLIGAR;

    while(1) {
        vTaskDelay(500 / portTICK_PERIOD_MS);

        led = !led;
        gpio_set_level(LED_ESP32, led);
    }
} //Final da TASK uart_task
