/////////////////////////////////////////////////////////////////////////////
//                   Nome da função: uart_task(void *arg)
//
// Proposito: Realiza monitoramento e a atuação na UART 1 e 2
//
//   Entrada: void *arg
//     Saída: NULL
/////////////////////////////////////////////////////////////////////////////
void uart_task(void *arg) {
     uart_write_bytes(UART_NUM_1, "\r\n BOTAO DESATIVADO \r\n", 22);
    while (1) {
        // Configure a temporary buffer for the incoming data
        uint8_t input4 = gpio_get_level(INP4);
        if (!input4) {
            uart_write_bytes(UART_NUM_1, "BOTAO ATIVADO \r\n", 16);
            uint8_t led = DESLIGAR;
            for (uint8_t i = 0; i < 20; i++) {
                vTaskDelay(300 / portTICK_PERIOD_MS);
                led = !led;
                gpio_set_level(LED_ESP32, led);
                if (led == LIGAR) {
                    uart_write_bytes(UART_NUM_1, "LED ACESO \r\n", 12);
                } else {
                    uart_write_bytes(UART_NUM_1, "LED APAGADO \r\n", 14);
                }
            }
            uart_write_bytes(UART_NUM_1, "BOTAO DESATIVADO \r\n", 19);
        }
        vTaskDelay(1000 / portTICK_PERIOD_MS);
    }
} //Final da TASK uart_task
